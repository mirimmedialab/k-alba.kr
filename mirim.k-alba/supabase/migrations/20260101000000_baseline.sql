CREATE OR REPLACE FUNCTION public.find_users_to_notify_for_job(job_id bigint)
 RETURNS TABLE(user_id uuid, token text, platform text, distance_km numeric, search_radius_km integer, name text)
 LANGUAGE sql
 STABLE
AS $function$
  SELECT
    p.id AS user_id,
    pt.token,
    pt.platform,
    ROUND(
      (ST_Distance(
        p.home_geog,
        (SELECT geog FROM jobs WHERE id = job_id)
      ) / 1000)::NUMERIC, 2
    ) AS distance_km,
    p.search_radius_km,
    p.name
  FROM profiles p
  JOIN push_tokens pt ON pt.user_id = p.id AND pt.active = true
  LEFT JOIN notification_preferences np ON np.user_id = p.id
  WHERE
    p.home_geog IS NOT NULL
    AND p.location_opted_in = true
    AND COALESCE(np.nearby_jobs, true) = true
    AND ST_DWithin(
      p.home_geog,
      (SELECT geog FROM jobs WHERE id = job_id),
      COALESCE(p.search_radius_km, 10) * 1000
    )
    AND EXISTS (
      SELECT 1 FROM jobs j
      WHERE j.id = job_id
        AND (
          -- 비자 필터: 사용자 비자가 공고의 visa_types에 있거나, 공고가 특정 비자 요구 안 함
          p.visa IS NULL
          OR j.visa_types IS NULL
          OR p.visa = ANY(j.visa_types)
        )
    );
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public', 'auth'
AS $function$
BEGIN
  -- profiles 행 자동 생성
  -- ON CONFLICT DO NOTHING: 콜백에서 UPSERT가 먼저 들어와도 충돌 없음
  -- 예외 발생 시 trigger 자체는 통과시켜 auth.users INSERT를 막지 않음
  BEGIN
    INSERT INTO public.profiles (id, email, name, user_type)
    VALUES (
      NEW.id,
      NEW.email,
      COALESCE(
        NEW.raw_user_meta_data->>'name',
        NEW.raw_user_meta_data->>'full_name',
        split_part(COALESCE(NEW.email, 'user@unknown.com'), '@', 1)
      ),
      COALESCE(NEW.raw_user_meta_data->>'user_type', 'worker')
    )
    ON CONFLICT (id) DO NOTHING;
  EXCEPTION WHEN OTHERS THEN
    -- profiles INSERT 실패해도 auth.users INSERT는 성공시킴
    -- 콜백 페이지(/auth/callback)가 profiles UPSERT로 보강
    RAISE WARNING 'handle_new_user: profiles INSERT failed for user %: %', NEW.id, SQLERRM;
  END;

  RETURN NEW;
END;
$function$
;

