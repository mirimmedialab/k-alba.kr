CREATE OR REPLACE FUNCTION public.use_staff_invitation_token(p_token text, p_action text, p_reason text DEFAULT NULL::text, p_note text DEFAULT NULL::text, p_ip text DEFAULT NULL::text, p_user_agent text DEFAULT NULL::text)
 RETURNS TABLE(success boolean, message text, application_id uuid)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_record  RECORD;
  v_app_id  UUID;
BEGIN
  -- 토큰 검증
  SELECT * INTO v_record FROM staff_invitation_tokens WHERE token = p_token;

  IF NOT FOUND THEN
    RETURN QUERY SELECT false, 'token_not_found'::TEXT, NULL::UUID;
    RETURN;
  END IF;

  IF v_record.expires_at < now() THEN
    RETURN QUERY SELECT false, 'expired'::TEXT, v_record.application_id;
    RETURN;
  END IF;

  IF v_record.used_at IS NOT NULL THEN
    RETURN QUERY SELECT false, 'already_used'::TEXT, v_record.application_id;
    RETURN;
  END IF;

  -- 액션 검증
  IF p_action NOT IN ('approved', 'rejected', 'declined', 'request_docs') THEN
    RETURN QUERY SELECT false, 'invalid_action'::TEXT, v_record.application_id;
    RETURN;
  END IF;

  v_app_id := v_record.application_id;

  -- 토큰 사용 처리
  UPDATE staff_invitation_tokens
  SET used_at = now(),
      used_action = p_action,
      decline_reason = p_reason,
      reviewer_note = p_note,
      ip_address = p_ip::INET,
      user_agent = p_user_agent
  WHERE token = p_token;

  -- partwork_applications 상태 업데이트
  IF p_action = 'approved' THEN
    UPDATE partwork_applications
    SET status = 'reviewing',                   -- 학교 승인 → 출입국청 단계로
        approved_at = now(),                    -- 학교 승인 시각
        university_certified = true,
        reviewer_note = p_note,
        updated_at = now()
    WHERE id = v_app_id;
  ELSIF p_action = 'rejected' THEN
    UPDATE partwork_applications
    SET status = 'rejected',
        rejected_at = now(),
        rejection_reason = COALESCE(p_reason, p_note),
        updated_at = now()
    WHERE id = v_app_id;
  ELSIF p_action = 'declined' THEN
    -- 학교 측이 검토 자체를 거부 → 학생에게 다른 경로 안내
    UPDATE partwork_applications
    SET school_status = 'self_print',           -- 학교 직접 방문으로 전환
        updated_at = now()
    WHERE id = v_app_id;
  ELSIF p_action = 'request_docs' THEN
    -- 추가 서류 요청 (status는 documents_needed로 통일)
    -- 9번 마이그레이션이 정의한 컬럼: documents_requested_at, documents_request_note, requested_documents
    UPDATE partwork_applications
    SET status = 'documents_needed',
        documents_requested_at = now(),
        documents_request_note = p_note,
        updated_at = now()
    WHERE id = v_app_id;
  END IF;

  -- partwork_review_log에도 기록 (감사용)
  -- ⚠️ partwork_review_log.action CHECK는 ('opened','requested_docs','signed','approved','rejected','commented')만 허용
  -- staff invitation 액션을 매핑: declined/request_docs를 호환되는 값으로 변환
  INSERT INTO partwork_review_log (application_id, action, note, ip_address, user_agent)
  VALUES (
    v_app_id,
    CASE p_action
      WHEN 'request_docs' THEN 'requested_docs'  -- 명명 일치
      WHEN 'declined'     THEN 'commented'       -- 학교가 검토 거부 = 의견 달기로 매핑
      ELSE p_action                              -- approved, rejected는 그대로
    END,
    p_note, p_ip, p_user_agent
  );

  RETURN QUERY SELECT true, p_action::TEXT, v_app_id;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.validate_university_email(email text)
 RETURNS boolean
 LANGUAGE plpgsql
 IMMUTABLE
AS $function$
BEGIN
  IF email IS NULL OR length(email) > 255 THEN
    RETURN false;
  END IF;
  -- 일반 이메일 형식 + 학술 도메인
  RETURN email ~* '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.(ac\.kr|edu|ac\.[a-z]{2,3}|edu\.[a-z]{2,3})$';
END;
$function$
;

CREATE OR REPLACE FUNCTION public.verify_email_domain(p_email text, p_university_id bigint)
 RETURNS boolean
 LANGUAGE plpgsql
 IMMUTABLE
AS $function$
DECLARE
  email_domain TEXT;
  allowed TEXT[];
BEGIN
  -- 이메일에서 도메인 추출
  email_domain := lower(split_part(p_email, '@', 2));
  IF email_domain = '' THEN RETURN false; END IF;

  -- 허용 도메인 조회
  SELECT allowed_email_domains INTO allowed
  FROM universities WHERE id = p_university_id;

  -- 빈 배열이거나 NULL이면 거부 (운영팀이 도메인 미설정한 경우 안전 차단)
  IF allowed IS NULL OR array_length(allowed, 1) IS NULL THEN
    RETURN false;
  END IF;

  -- 정확 매칭 또는 서브도메인 매칭
  RETURN EXISTS (
    SELECT 1 FROM unnest(allowed) AS d
    WHERE email_domain = d OR email_domain LIKE '%.' || d
  );
END;
$function$
;

CREATE OR REPLACE FUNCTION public.verify_staff_invitation_token(p_token text)
 RETURNS TABLE(is_valid boolean, reason text, application_id uuid, invited_email text)
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_record RECORD;
BEGIN
