CREATE OR REPLACE FUNCTION public.issue_staff_invitation_token(p_application_id uuid, p_email text, p_name text DEFAULT NULL::text)
 RETURNS text
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  v_token         TEXT;
  v_domain        TEXT;
  v_expires_at    TIMESTAMPTZ;
  v_existing      INT;
BEGIN
  -- 이메일 형식 검증
  IF NOT validate_university_email(p_email) THEN
    RAISE EXCEPTION '학교 이메일 형식이 올바르지 않습니다 (.ac.kr 또는 .edu): %', p_email;
  END IF;

  -- 같은 application_id + email 조합으로 활성 토큰이 있으면 재사용 (스팸 방지)
  SELECT count(*) INTO v_existing
  FROM staff_invitation_tokens
  WHERE application_id = p_application_id
    AND invited_email = lower(p_email)
    AND used_at IS NULL
    AND expires_at > now();

  IF v_existing > 0 THEN
    -- 기존 토큰 반환
    SELECT token INTO v_token
    FROM staff_invitation_tokens
    WHERE application_id = p_application_id
      AND invited_email = lower(p_email)
      AND used_at IS NULL
      AND expires_at > now()
    ORDER BY created_at DESC
    LIMIT 1;
    RETURN v_token;
  END IF;

  -- 도메인 추출
  v_domain := substring(lower(p_email) FROM '@(.+)$');
  v_expires_at := now() + INTERVAL '7 days';

  -- pgcrypto의 gen_random_bytes로 32자 랜덤 생성
  v_token := encode(gen_random_bytes(24), 'base64');
  -- base64는 + / = 포함 → URL safe로 변환
  v_token := replace(replace(replace(v_token, '+', '-'), '/', '_'), '=', '');

  INSERT INTO staff_invitation_tokens (
    token, application_id, invited_email, invited_name,
    email_domain, expires_at
  )
  VALUES (
    v_token, p_application_id, lower(p_email), p_name,
    v_domain, v_expires_at
  );

  -- partwork_applications 업데이트
  UPDATE partwork_applications
  SET school_status = 'invited',
      invited_staff_email = lower(p_email),
      invited_staff_name = p_name,
      invitation_sent_at = now(),
      updated_at = now()
  WHERE id = p_application_id;

  RETURN v_token;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.jobs_nearby(user_lat double precision, user_lng double precision, radius_km double precision DEFAULT 10, visa_filter text[] DEFAULT NULL::text[], limit_count integer DEFAULT 50)
 RETURNS TABLE(id bigint, title text, company_name text, address text, sigungu text, job_type text, pay_type text, pay_amount integer, visa_types text[], latitude double precision, longitude double precision, distance_m double precision, distance_km numeric, provides_housing boolean, provides_shuttle boolean, nearest_station text, walk_to_station_min integer, transit_info jsonb, created_at timestamp with time zone)
 LANGUAGE sql
 STABLE
AS $function$
  SELECT
    j.id,
    j.title,
    p.company_name,          -- profiles에서 join
    j.address,
    j.sigungu,
    j.job_type,
    j.pay_type,
    j.pay_amount,
    -- visa_types는 migration-visa-types.sql 에서 TEXT[]로 변환됨
    j.visa_types,
    j.latitude,
    j.longitude,
    ST_Distance(
      j.geog,
      ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography
    ) AS distance_m,
    ROUND(
      (ST_Distance(
        j.geog,
        ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography
      ) / 1000)::numeric,
      2
    ) AS distance_km,
    j.provides_housing,
    j.provides_shuttle,
    j.nearest_station,
    j.walk_to_station_min,
    j.transit_info,
    j.created_at
  FROM jobs j
  LEFT JOIN profiles p ON p.id = j.employer_id
  WHERE
    j.geog IS NOT NULL
    AND j.status = 'active'
    AND ST_DWithin(
      j.geog,
      ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography,
      radius_km * 1000
    )
    AND (visa_filter IS NULL OR j.visa_types && visa_filter)
  ORDER BY
    j.geog <-> ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography
  LIMIT limit_count;
$function$
;

CREATE OR REPLACE FUNCTION public.jobs_recommended(user_lat double precision, user_lng double precision, user_visa text DEFAULT NULL::text, user_korean_level text DEFAULT NULL::text, user_transport text[] DEFAULT ARRAY['transit'::text, 'walk'::text], radius_km double precision DEFAULT 20, limit_count integer DEFAULT 30)
 RETURNS TABLE(id bigint, title text, company_name text, address text, sigungu text, job_type text, pay_type text, pay_amount integer, visa_types text[], korean_level text, latitude double precision, longitude double precision, distance_m double precision, distance_km numeric, provides_housing boolean, provides_shuttle boolean, nearest_station text, walk_to_station_min integer, transit_info jsonb, created_at timestamp with time zone, score_distance numeric, score_transit numeric, score_visa numeric, score_korean numeric, score_housing numeric, score_total numeric, reason text)
 LANGUAGE sql
 STABLE
AS $function$
WITH base AS (
  SELECT
    j.*,
    p.company_name AS employer_company,
    ST_Distance(
      j.geog,
      ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography
    ) AS distance_meters
  FROM jobs j
  LEFT JOIN profiles p ON p.id = j.employer_id
  WHERE
    j.geog IS NOT NULL
    AND j.status = 'active'
    AND ST_DWithin(
      j.geog,
      ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography,
      radius_km * 1000
    )
),
scored AS (
  SELECT
    b.*,
    CASE
      WHEN b.distance_meters <= 1000 THEN 40
      WHEN b.distance_meters >= radius_km * 1000 THEN 0
      ELSE 40 * (1 - (b.distance_meters - 1000) / (radius_km * 1000 - 1000))
    END::NUMERIC AS score_distance_val,

    CASE
      WHEN b.provides_shuttle THEN 20
      WHEN b.walk_to_station_min IS NOT NULL AND b.walk_to_station_min <= 5 THEN 18
      WHEN b.walk_to_station_min IS NOT NULL AND b.walk_to_station_min <= 10 THEN 12
      WHEN b.walk_to_station_min IS NOT NULL AND b.walk_to_station_min <= 15 THEN 8
      ELSE GREATEST(0, 10 - (b.distance_meters / 2000))
    END::NUMERIC AS score_transit_val,

    CASE
      WHEN user_visa IS NULL THEN 10
      WHEN user_visa = ANY(b.visa_types) THEN 20
      ELSE 0
    END::NUMERIC AS score_visa_val,

    CASE
      WHEN user_korean_level IS NULL THEN 5
      WHEN b.korean_level IS NULL OR b.korean_level = '' THEN 10
      WHEN user_korean_level = 'advanced' THEN 10
      WHEN user_korean_level = 'intermediate' AND b.korean_level IN ('beginner', 'intermediate', 'none') THEN 10
      WHEN user_korean_level = 'beginner' AND b.korean_level IN ('beginner', 'none') THEN 10
      WHEN user_korean_level = 'none' AND b.korean_level = 'none' THEN 10
      ELSE 0
    END::NUMERIC AS score_korean_val,

    CASE
      WHEN b.provides_housing AND user_visa IN ('E-9','E-8','H-2') THEN 10
      WHEN b.provides_housing THEN 5
      ELSE 0
    END::NUMERIC AS score_housing_val

  FROM base b
)
SELECT
  s.id,
  s.title,
  s.employer_company AS company_name,
  s.address,
  s.sigungu,
  s.job_type,
  s.pay_type,
  s.pay_amount,
  s.visa_types,
  s.korean_level,
  s.latitude,
  s.longitude,
  s.distance_meters AS distance_m,
  ROUND((s.distance_meters / 1000)::NUMERIC, 2) AS distance_km,
  s.provides_housing,
  s.provides_shuttle,
  s.nearest_station,
  s.walk_to_station_min,
  s.transit_info,
  s.created_at,
  ROUND(s.score_distance_val, 1) AS score_distance,
  ROUND(s.score_transit_val, 1)  AS score_transit,
  ROUND(s.score_visa_val, 1)     AS score_visa,
  ROUND(s.score_korean_val, 1)   AS score_korean,
  ROUND(s.score_housing_val, 1)  AS score_housing,
  ROUND(
    s.score_distance_val + s.score_transit_val +
    s.score_visa_val + s.score_korean_val + s.score_housing_val,
    1
  ) AS score_total,
  TRIM(CONCAT_WS(' · ',
    CASE WHEN s.distance_meters <= 2000 THEN '🏠 집에서 가까움' END,
    CASE WHEN s.provides_housing AND user_visa IN ('E-9','E-8','H-2') THEN '🛏️ 숙식 제공' END,
    CASE WHEN s.provides_shuttle THEN '🚐 통근버스' END,
    CASE WHEN user_visa = ANY(s.visa_types) THEN '✓ 내 비자 가능' END,
    CASE WHEN s.walk_to_station_min IS NOT NULL AND s.walk_to_station_min <= 5 THEN '🚇 역 도보 5분' END
  )) AS reason
FROM scored s
ORDER BY
  (s.score_distance_val + s.score_transit_val +
   s.score_visa_val + s.score_korean_val + s.score_housing_val) DESC,
  s.distance_meters ASC
LIMIT limit_count;
$function$
;

CREATE OR REPLACE FUNCTION public.jobs_stats_by_region(region_sigungu text)
 RETURNS TABLE(total_active bigint, avg_pay_hourly numeric, top_job_types text[])
 LANGUAGE sql
 STABLE
AS $function$
  SELECT
    COUNT(*) FILTER (WHERE status = 'active')          AS total_active,
    ROUND(AVG(pay_amount) FILTER (WHERE pay_type = '시급'), 0) AS avg_pay_hourly,
    ARRAY(
      SELECT job_type FROM jobs
      WHERE sigungu = region_sigungu AND status = 'active'
      GROUP BY job_type
      ORDER BY COUNT(*) DESC
      LIMIT 3
    ) AS top_job_types
  FROM jobs
  WHERE sigungu = region_sigungu;
$function$
;

CREATE OR REPLACE FUNCTION public.on_partwork_approved_create_imm()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  -- 'approved' 상태로 변경되는 순간에만 작동
  IF NEW.status = 'approved' AND (OLD.status IS NULL OR OLD.status != 'approved') THEN
    -- 이미 통합신청서가 있으면 생성하지 않음 (중복 방지)
    IF NOT EXISTS (
      SELECT 1 FROM immigration_applications
      WHERE partwork_application_id = NEW.id
    ) THEN
      PERFORM create_immigration_app_from_partwork(NEW.id);
    END IF;
  END IF;
  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.on_staff_signature_apply()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  -- 서명이 새로 추가됐고 status가 reviewing이면 → signed로 자동 변경
  IF NEW.staff_signature IS NOT NULL AND OLD.staff_signature IS NULL THEN
    NEW.staff_signature_at = now();
    IF NEW.status = 'reviewing' OR NEW.status = 'submitted' OR NEW.status = 'documents_needed' THEN
      NEW.status = 'signed';
    END IF;
  END IF;
  -- 승인 시각 자동
  IF NEW.status = 'approved' AND OLD.status != 'approved' THEN
    NEW.approved_at = now();
  END IF;
  -- 반려 시각 자동
  IF NEW.status = 'rejected' AND OLD.status != 'rejected' THEN
    NEW.rejected_at = now();
  END IF;
  RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.partwork_update_timestamp()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  NEW.updated_at := now();

  -- 상태 변경 시 해당 타임스탬프 자동 설정
  IF OLD.status IS DISTINCT FROM NEW.status THEN
    IF NEW.status = 'submitted' AND OLD.submitted_at IS NULL THEN
      NEW.submitted_at := now();
    END IF;
    IF NEW.status IN ('approved', 'rejected') AND OLD.reviewed_at IS NULL THEN
      NEW.reviewed_at := now();
    END IF;
  END IF;

  RETURN NEW;
END;
$function$
;

